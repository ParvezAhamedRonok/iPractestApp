                    <p class="text-gray-700 text-lg">Microplastics are tiny pieces of plastic smaller than five millimetres in size. Recently there’s been a greater awareness that there are large quantities of plastic waste – big and small – in the environment. The amount of plastic waste in the oceans has received widespread attention, but far less is known about the effects of microplastics in freshwater and particularly in soil.</p>

